//push function
let fruits = ["apple", "banana"];
let newLength = fruits.push("apple");
console.log(newLength)
console.log(fruits)

//pop function
let fruits1 = ["apple", "banana", "mango"];
let removed = fruits1.pop();
console.log(removed)
console.log(fruits1)

//shift function
let fruits2 = ["apple", "banana", "mango"];
let removed1 = fruits2.shift();
console.log(removed1)
console.log(fruits2)

//unshift function
let fruits3 = ["apple", "banana"];
let newLength1 = fruits3.unshift("mango");
console.log(newLength1)
console.log(fruits3)        
